"""
Evaluation module for synthetic tabular data.

Implements five complementary metrics for assessing the quality of
synthetic data generated by CTGAN:

1. Wasserstein distance - continuous variable distributions
2. Jensen-Shannon divergence - categorical variable distributions
3. Frobenius norm of correlation difference - multivariate structure
4. TSTR (Train on Synthetic, Test on Real) - predictive utility
5. DCR (Distance to Closest Record) - memorization detection

Each metric is computed by an isolated function returning structured
results, plus a high-level `evaluate_all` function that runs the
complete suite.
"""

from typing import Dict, List

import numpy as np
import pandas as pd
from scipy.spatial.distance import jensenshannon
from scipy.stats import wasserstein_distance
from sklearn.compose import ColumnTransformer
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score, f1_score, roc_auc_score
from sklearn.neighbors import NearestNeighbors
from sklearn.pipeline import Pipeline
from sklearn.preprocessing import OneHotEncoder, StandardScaler

from src.config import (
    CATEGORICAL_COLUMNS,
    CONTINUOUS_COLUMNS,
    RANDOM_SEED,
    TARGET_COLUMN,
)


# ============================================================
# 1. WASSERSTEIN DISTANCE (continuous variables)
# ============================================================

def compute_wasserstein(
    real: pd.DataFrame,
    synthetic: pd.DataFrame,
    columns: List[str] = None,
) -> pd.DataFrame:
    """
    Compute Wasserstein distance for each continuous variable.

    Wasserstein (earth mover's distance) measures the minimum cost
    to transform one distribution into another. Reported in absolute
    units and as percentage of the variable range for cross-variable
    comparison.

    Parameters
    ----------
    real : pd.DataFrame
        Real dataset.
    synthetic : pd.DataFrame
        Synthetic dataset.
    columns : list, optional
        Continuous columns to evaluate. Defaults to CONTINUOUS_COLUMNS.

    Returns
    -------
    pd.DataFrame
        Columns: 'variable', 'wasserstein', 'range', 'wasserstein_pct'.
    """
    if columns is None:
        columns = CONTINUOUS_COLUMNS

    rows = []
    for col in columns:
        real_vals = real[col].values
        synth_vals = synthetic[col].values

        w_dist = wasserstein_distance(real_vals, synth_vals)
        var_range = real_vals.max() - real_vals.min()
        w_pct = (w_dist / var_range * 100) if var_range > 0 else 0.0

        rows.append({
            "variable": col,
            "wasserstein": round(w_dist, 4),
            "range": int(var_range),
            "wasserstein_pct": round(w_pct, 2),
        })

    return pd.DataFrame(rows)


# ============================================================
# 2. JENSEN-SHANNON DIVERGENCE (categorical variables)
# ============================================================

def compute_jsd(
    real: pd.DataFrame,
    synthetic: pd.DataFrame,
    columns: List[str] = None,
) -> pd.DataFrame:
    """
    Compute Jensen-Shannon divergence for each categorical variable.

    JSD is symmetric, bounded in [0, 1] (with base-2 logarithm),
    and well-defined even when probabilities are zero. It is the
    squared Jensen-Shannon distance returned by scipy.

    Parameters
    ----------
    real : pd.DataFrame
        Real dataset.
    synthetic : pd.DataFrame
        Synthetic dataset.
    columns : list, optional
        Categorical columns to evaluate. Defaults to CATEGORICAL_COLUMNS.

    Returns
    -------
    pd.DataFrame
        Columns: 'variable', 'cardinality', 'jsd'.
    """
    if columns is None:
        columns = CATEGORICAL_COLUMNS

    rows = []
    for col in columns:
        real_dist = real[col].value_counts(normalize=True)
        synth_dist = synthetic[col].value_counts(normalize=True)

        # Align categories: union of both, fill missing with 0
        all_categories = sorted(set(real_dist.index) | set(synth_dist.index))
        real_probs = real_dist.reindex(all_categories, fill_value=0).values
        synth_probs = synth_dist.reindex(all_categories, fill_value=0).values

        # scipy returns the JS distance (sqrt of divergence); square it
        jsd = jensenshannon(real_probs, synth_probs, base=2) ** 2

        rows.append({
            "variable": col,
            "cardinality": real[col].nunique(),
            "jsd": round(jsd, 4),
        })

    return pd.DataFrame(rows)


# ============================================================
# 3. FROBENIUS NORM (correlation matrix difference)
# ============================================================

def compute_correlation_difference(
    real: pd.DataFrame,
    synthetic: pd.DataFrame,
    columns: List[str] = None,
) -> Dict[str, object]:
    """
    Compare correlation matrices via Frobenius norm.

    Measures how well the synthetic data preserves bivariate
    dependencies between continuous variables.

    Parameters
    ----------
    real : pd.DataFrame
        Real dataset.
    synthetic : pd.DataFrame
        Synthetic dataset.
    columns : list, optional
        Continuous columns. Defaults to CONTINUOUS_COLUMNS.

    Returns
    -------
    dict
        Keys: 'corr_real', 'corr_synthetic', 'difference',
        'frobenius_absolute', 'frobenius_relative_pct'.
    """
    if columns is None:
        columns = CONTINUOUS_COLUMNS

    corr_real = real[columns].corr()
    corr_synth = synthetic[columns].corr()
    diff = corr_real - corr_synth

    frobenius_abs = np.linalg.norm(diff.values, ord="fro")
    frobenius_real_norm = np.linalg.norm(corr_real.values, ord="fro")
    frobenius_rel_pct = (frobenius_abs / frobenius_real_norm * 100) if frobenius_real_norm > 0 else 0.0

    return {
        "corr_real": corr_real,
        "corr_synthetic": corr_synth,
        "difference": diff,
        "frobenius_absolute": round(frobenius_abs, 4),
        "frobenius_relative_pct": round(frobenius_rel_pct, 2),
    }


# ============================================================
# 4. TSTR (Train on Synthetic, Test on Real)
# ============================================================

def _build_classifier_pipeline(
    categorical_features: List[str],
    continuous_features: List[str],
) -> Pipeline:
    """
    Build a sklearn pipeline: OneHot for categoricals + RandomForest.

    Internal helper used by both TSTR and baseline evaluation.
    """
    preprocessor = ColumnTransformer(
        transformers=[
            ("cat", OneHotEncoder(handle_unknown="ignore", sparse_output=False), categorical_features),
            ("num", "passthrough", continuous_features),
        ]
    )
    return Pipeline([
        ("preprocessor", preprocessor),
        ("classifier", RandomForestClassifier(
            n_estimators=100,
            random_state=RANDOM_SEED,
            n_jobs=-1,
        )),
    ])


def _evaluate_single_model(
    X_train: pd.DataFrame,
    y_train: pd.Series,
    X_test: pd.DataFrame,
    y_test: pd.Series,
    categorical_features: List[str],
    continuous_features: List[str],
    positive_label: str = ">50K",
) -> Dict[str, float]:
    """
    Train a Random Forest and return accuracy, F1, and AUC.
    Internal helper used by compute_tstr.
    """
    pipeline = _build_classifier_pipeline(categorical_features, continuous_features)
    pipeline.fit(X_train, y_train)

    y_pred = pipeline.predict(X_test)
    y_proba = pipeline.predict_proba(X_test)[:, 1]
    y_test_bin = (y_test == positive_label).astype(int)

    return {
        "accuracy": round(accuracy_score(y_test, y_pred), 4),
        "f1": round(f1_score(y_test, y_pred, pos_label=positive_label), 4),
        "auc": round(roc_auc_score(y_test_bin, y_proba), 4),
    }


def compute_tstr(
    real_train: pd.DataFrame,
    synthetic: pd.DataFrame,
    holdout: pd.DataFrame,
    target_col: str = TARGET_COLUMN,
    verbose: bool = True,
) -> pd.DataFrame:
    """
    Train on Synthetic, Test on Real - utility evaluation.

    Compares two classifiers on the same holdout:
    - BASELINE: trained on real data
    - TSTR: trained on synthetic data

    The ratio TSTR/BASELINE quantifies how much predictive utility
    is preserved by the synthetic dataset.

    Parameters
    ----------
    real_train : pd.DataFrame
        Real training data (the same used to train CTGAN).
    synthetic : pd.DataFrame
        Synthetic data generated by CTGAN.
    holdout : pd.DataFrame
        Real data unseen by CTGAN, used as test set.
    target_col : str
        Name of the target column.
    verbose : bool
        Print per-model metrics.

    Returns
    -------
    pd.DataFrame
        Columns: 'metric', 'baseline', 'tstr', 'ratio', 'loss_pct'.
    """
    # Define feature splits
    cat_features = [c for c in CATEGORICAL_COLUMNS if c != target_col]
    num_features = CONTINUOUS_COLUMNS

    # Datasets
    X_real, y_real = real_train.drop(columns=[target_col]), real_train[target_col]
    X_synth, y_synth = synthetic.drop(columns=[target_col]), synthetic[target_col]
    X_holdout, y_holdout = holdout.drop(columns=[target_col]), holdout[target_col]

    if verbose:
        print(f"Baseline: train on {len(X_real)} real → test on {len(X_holdout)} holdout")
    baseline = _evaluate_single_model(
        X_real, y_real, X_holdout, y_holdout, cat_features, num_features
    )

    if verbose:
        print(f"TSTR: train on {len(X_synth)} synthetic → test on {len(X_holdout)} holdout")
    tstr = _evaluate_single_model(
        X_synth, y_synth, X_holdout, y_holdout, cat_features, num_features
    )

    rows = []
    for metric in ["accuracy", "f1", "auc"]:
        b = baseline[metric]
        t = tstr[metric]
        ratio = t / b if b > 0 else 0
        loss = (1 - ratio) * 100
        rows.append({
            "metric": metric,
            "baseline": b,
            "tstr": t,
            "ratio": round(ratio, 4),
            "loss_pct": round(loss, 2),
        })

    return pd.DataFrame(rows)


# ============================================================
# 5. DCR (Distance to Closest Record) - memorization detection
# ============================================================

def compute_dcr(
    real_train: pd.DataFrame,
    synthetic: pd.DataFrame,
    holdout: pd.DataFrame,
    n_samples: int = 1000,
    columns: List[str] = None,
) -> Dict[str, float]:
    """
    Distance to Closest Record - detects memorization.

    For each synthetic sample, compute the distance to its nearest
    real neighbor. If synthetic samples are too close to training
    records compared to holdout records, the model is memorizing
    rather than generalizing.

    Reports the median DCR for two comparisons:
    - Synthetic vs Train: should NOT be much smaller than...
    - Synthetic vs Holdout: ...this baseline.

    Ratio close to 1.0 indicates good generalization.
    Ratio << 1.0 indicates memorization risk.

    Parameters
    ----------
    real_train : pd.DataFrame
        Real training data (CTGAN saw this).
    synthetic : pd.DataFrame
        Synthetic data.
    holdout : pd.DataFrame
        Real data CTGAN did NOT see.
    n_samples : int
        Synthetic samples to evaluate (for tractable computation).
    columns : list, optional
        Continuous columns to use for distance computation.

    Returns
    -------
    dict
        Keys: 'dcr_to_train_median', 'dcr_to_holdout_median', 'ratio'.
    """
    if columns is None:
        columns = CONTINUOUS_COLUMNS

    # Use only continuous columns for distance, standardized
    scaler = StandardScaler()
    real_scaled = scaler.fit_transform(real_train[columns])
    holdout_scaled = scaler.transform(holdout[columns])

    # Subsample synthetic for computational efficiency
    np.random.seed(RANDOM_SEED)
    n_eval = min(n_samples, len(synthetic))
    idx = np.random.choice(len(synthetic), size=n_eval, replace=False)
    synth_scaled = scaler.transform(synthetic.iloc[idx][columns])

    # Nearest neighbor in train
    nn_train = NearestNeighbors(n_neighbors=1, n_jobs=-1).fit(real_scaled)
    dcr_train, _ = nn_train.kneighbors(synth_scaled)

    # Nearest neighbor in holdout
    nn_holdout = NearestNeighbors(n_neighbors=1, n_jobs=-1).fit(holdout_scaled)
    dcr_holdout, _ = nn_holdout.kneighbors(synth_scaled)

    median_train = float(np.median(dcr_train))
    median_holdout = float(np.median(dcr_holdout))
    ratio = median_train / median_holdout if median_holdout > 0 else 0

    return {
        "dcr_to_train_median": round(median_train, 4),
        "dcr_to_holdout_median": round(median_holdout, 4),
        "ratio": round(ratio, 4),
    }


# ============================================================
# HIGH-LEVEL: COMPLETE EVALUATION
# ============================================================

def evaluate_all(
    real_train: pd.DataFrame,
    synthetic: pd.DataFrame,
    holdout: pd.DataFrame,
    verbose: bool = True,
) -> Dict[str, object]:
    """
    Run the complete evaluation suite.

    Parameters
    ----------
    real_train : pd.DataFrame
        Real training data used to fit CTGAN.
    synthetic : pd.DataFrame
        Synthetic data from CTGAN.
    holdout : pd.DataFrame
        Real data unseen by CTGAN, for unbiased TSTR and DCR.
    verbose : bool
        Print progress and intermediate results.

    Returns
    -------
    dict
        Keys: 'wasserstein', 'jsd', 'correlation', 'tstr', 'dcr'.
        Each value is the structured result of the corresponding metric.
    """
    if verbose:
        print("\n" + "=" * 60)
        print("COMPLETE EVALUATION SUITE")
        print("=" * 60)

    if verbose:
        print("\n[1/5] Computing Wasserstein distances (continuous)...")
    wasserstein_df = compute_wasserstein(real_train, synthetic)

    if verbose:
        print("[2/5] Computing Jensen-Shannon divergences (categorical)...")
    jsd_df = compute_jsd(real_train, synthetic)

    if verbose:
        print("[3/5] Computing correlation matrix difference (Frobenius)...")
    corr_results = compute_correlation_difference(real_train, synthetic)

    if verbose:
        print("[4/5] Computing TSTR utility...")
    tstr_df = compute_tstr(real_train, synthetic, holdout, verbose=verbose)

    if verbose:
        print("[5/5] Computing DCR (memorization check)...")
    dcr_results = compute_dcr(real_train, synthetic, holdout)

    if verbose:
        print("\n" + "=" * 60)
        print("EVALUATION COMPLETE")
        print("=" * 60)

    return {
        "wasserstein": wasserstein_df,
        "jsd": jsd_df,
        "correlation": corr_results,
        "tstr": tstr_df,
        "dcr": dcr_results,
    }